package switchcase;

public class SwitchCharTest {
    public static void main(String[] args) {
        String str = "test";
        switch (str) {
        case "a":
            System.out.println("a");
            break;
        case "b":
            System.out.println("b");
            break;
        case "c":
            System.out.println("c");
            break;
        default:
            System.out.println("c");
            break;
        }
    }
}
